// ---------- Data Classes (converted to JS objects) ----------
class SingleSchedule {
  constructor(day, timing, room = "NA") {
    this.day = day;
    this.timing = timing; // [startDate, endDate]
    this.room = room;
  }
}

class Lecture {
  constructor(section, teacher = "TBA", courseName = "") {
    this.teacher = teacher;
    this.schedule = [];
    this.section = section;
    this.courseName = courseName;
  }
}

class Course {
  constructor(name) {
    this.name = name;
    this.lectures = [];
  }
}

// ---------- Utility functions ----------
function parseTimeSlot(timeStr) {
  const days = ["Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"];
  const parts = timeStr.split(/<br\s*\/?>/i).map(p => p.trim()).filter(Boolean);
  const results = [];

  for (let part of parts) {
    let isLab = part.toLowerCase().startsWith("lab");
    if (isLab) part = part.slice(3).trim();

    const foundDays = days.filter(d => part.includes(d));
    if (foundDays.length === 0) continue;

    const roomMatch = part.match(/(SST\d+-\d+[A-Z]?)/);
    const room = roomMatch ? roomMatch[1] : null;

    const timePattern = /(\d{1,2}:\d{2}\s*[AP]M)\s*-\s*(\d{1,2}:\d{2}\s*[AP]M)/gi;
    let matches;
    while ((matches = timePattern.exec(part)) !== null) {
      const [_, startTime, endTime] = matches;
      for (let day of foundDays) {
        try {
          const startDt = new Date(`2023-01-02T${convertTo24Hr(startTime)}`); // dummy date
          const endDt = new Date(`2023-01-02T${convertTo24Hr(endTime)}`);
          results.push(new SingleSchedule(day, [startDt, endDt], room));
        } catch (e) {
          console.warn(`⚠️ Could not parse '${part}':`, e);
        }
      }
    }
  }
  return results;
}

function convertTo24Hr(timeStr) {
  // Convert "02:00 PM" -> "14:00:00"
  const [time, modifier] = timeStr.trim().split(" ");
  let [hours, minutes] = time.split(":").map(Number);
  if (modifier.toUpperCase() === "PM" && hours < 12) hours += 12;
  if (modifier.toUpperCase() === "AM" && hours === 12) hours = 0;
  return `${String(hours).padStart(2,"0")}:${String(minutes).padStart(2,"0")}:00`;
}

function hasTimeConflict(schedule1, schedule2) {
  for (let s1 of schedule1) {
    for (let s2 of schedule2) {
      if (s1.day === s2.day) {
        const [start1, end1] = s1.timing;
        const [start2, end2] = s2.timing;
        if (!(end1 <= start2 || end2 <= start1)) return true;
      }
    }
  }
  return false;
}

function hasDayOffConflict(lecture, daysOff) {
  return lecture.schedule.some(s => daysOff.includes(s.day));
}

function generateTimetables(courses, daysOff, currentSelection = [], index = 0) {
  if (index === courses.length) return [ [...currentSelection] ];

  let allTimetables = [];
  const course = courses[index];

  for (let lecture of course.lectures) {
    if (hasDayOffConflict(lecture, daysOff)) continue;

    let conflict = false;
    for (let selected of currentSelection) {
      if (hasTimeConflict(lecture.schedule, selected.schedule)) {
        conflict = true;
        break;
      }
    }

    if (!conflict) {
      currentSelection.push(lecture);
      allTimetables = allTimetables.concat(
        generateTimetables(courses, daysOff, currentSelection, index + 1)
      );
      currentSelection.pop();
    }
  }

  return allTimetables;
}

function timetableToReactFormat(timetable, tid) {
  return {
    id: tid,
    courses: timetable.map(lecture => ({
      name: lecture.courseName,
      section: lecture.section,
      teacher: lecture.teacher,
      schedule: lecture.schedule.map(s => ({
        day: s.day,
        time: `${formatTime(s.timing[0])}-${formatTime(s.timing[1])}`,
        room: s.room || "TBA"
      }))
    }))
  };
}

function formatTime(dateObj) {
  return dateObj.toLocaleTimeString([], {hour: "2-digit", minute: "2-digit"});
}

// ---------- Main execution function ----------
function generateTimetablesFromData(rawData, daysOff = []) {
  // Convert raw data into Course/Lecture objects
  const courses = rawData.map(courseData => {
    const courseName = Object.keys(courseData)[0];
    const c = new Course(courseName);

    for (let lectureData of courseData[courseName]) {
      const lec = new Lecture(
        lectureData.SectionName,
        lectureData.FacultyName || "TBA",
        c.name
      );
      lec.schedule = parseTimeSlot(lectureData.Details);
      c.lectures.push(lec);
    }
    return c;
  });

  const result = generateTimetables(courses, daysOff);
  return result.map((t, i) => timetableToReactFormat(t, i + 1));
}

